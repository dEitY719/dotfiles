#!/bin/sh
# lib/ssh_config.sh — regenerate the ssh config drop-in from the manifest.
#
# Writes ~/.ssh/config.d/devx-delegations (override: $DEVX_SSH_CONFIG_DROPIN)
# and adds a single `Include config.d/devx-delegations` line to ~/.ssh/config.
# AI ssh always goes through a manifest alias (issue #877 L3 allowlist).
#
# Sourced — POSIX sh only. Depends on lib/manifest.sh being sourced first.

ssh_config_dropin_path() {
    printf '%s\n' "${DEVX_SSH_CONFIG_DROPIN:-$HOME/.ssh/config.d/devx-delegations}"
}

ssh_config_main_path() {
    printf '%s\n' "${DEVX_SSH_CONFIG:-$HOME/.ssh/config}"
}

# Expand a leading ~ to $HOME (POSIX-safe, no eval).
ssh_config_expand_tilde() {
    # shellcheck disable=SC2088  # ~ patterns are matched literally, not expanded
    case "$1" in
    "~") printf '%s\n' "$HOME" ;;
    # The pattern in ${1#...} is quoted so the leading ~/ is stripped
    # literally — an unquoted ~/ would itself be tilde-expanded to $HOME/.
    "~/"*) printf '%s/%s\n' "$HOME" "${1#"~/"}" ;;
    *) printf '%s\n' "$1" ;;
    esac
}

# Regenerate the drop-in from all non-revoked manifest entries.
ssh_config_regen() {
    dropin="$(ssh_config_dropin_path)"
    dir="$(dirname "$dropin")"
    [ -d "$dir" ] || mkdir -p "$dir"
    shk="$(manifest_default strict_host_key_checking 2>/dev/null)"
    [ -n "$shk" ] || shk='yes'
    tmp="${dropin}.tmp.$$"
    {
        printf '# AUTO-GENERATED by devx:ssh-delegate — do not edit by hand.\n'
        printf '# Regenerated by sync/add/revoke. Source of truth: %s\n\n' "$(manifest_path)"
        manifest_to_tsv | awk -F'\t' '$11!="true" && NF {print $1}' | while IFS= read -r al; do
            [ -n "$al" ] || continue
            host="$(manifest_get "$al" host)"
            user="$(manifest_get "$al" user)"
            port="$(manifest_eff "$al" port port)"
            [ -n "$port" ] || port=22
            idf="$(manifest_eff "$al" identity_file identity_file)"
            idf="$(ssh_config_expand_tilde "$idf")"
            printf 'Host %s\n' "$al"
            printf '    HostName %s\n' "$host"
            printf '    User %s\n' "$user"
            printf '    Port %s\n' "$port"
            [ -n "$idf" ] && printf '    IdentityFile %s\n' "$idf"
            printf '    IdentitiesOnly yes\n'
            printf '    StrictHostKeyChecking %s\n\n' "$shk"
        done
    } >"$tmp"
    chmod 600 "$tmp"
    mv "$tmp" "$dropin"
}

# Ensure ~/.ssh/config includes the drop-in exactly once.
ssh_config_ensure_include() {
    main="$(ssh_config_main_path)"
    dropin="$(ssh_config_dropin_path)"
    dir="$(dirname "$main")"
    [ -d "$dir" ] || mkdir -p "$dir"
    [ -f "$main" ] || {
        : >"$main"
        chmod 600 "$main"
    }
    if ! grep -qF "Include $dropin" "$main" 2>/dev/null; then
        # Prepend Include — Host blocks before it would otherwise shadow it.
        tmp="${main}.tmp.$$"
        {
            printf 'Include %s\n\n' "$dropin"
            cat "$main"
        } >"$tmp"
        chmod 600 "$tmp"
        mv "$tmp" "$main"
    fi
}
