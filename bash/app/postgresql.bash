# -------------------------------
# 9) Create DB quickly from services[] (psql_create <DB_NAME>)
# Example)
# 9.1) dmc_playground_prod 생성/하드닝 (services에 이미 정의돼 있으므로 비번 프롬프트 없이 진행)
#   psql_create dmc_playground_prod
# 9.2) services에 없는 DB를 만들 때 (비번 입력 프롬프트가 뜸)
#   psql_create my_new_db
# -------------------------------
psql_create() {
    local DB_NAME="${1:-}"
    if [[ -z "$DB_NAME" ]]; then
        echo "Usage: psql_create <DB_NAME>"
        return 1
    fi

    # 9-1) services 배열에서 DB_NAME 매칭하여 app_user/app_pass 추출
    local APP_USER="" APP_PASS="" SERVICE_HIT=""
    for entry in "${services[@]}"; do
        # shellcheck disable=SC2086
        read -r svc db user pass <<<"$entry"
        if [[ "$db" == "$DB_NAME" ]]; then
            APP_USER="$user"
            APP_PASS="$pass"
            SERVICE_HIT="$svc"
            break
        fi
    done
    # 매칭 실패 시 기본값/프롬프트
    if [[ -z "$APP_USER" ]]; then
        APP_USER="dmc_user"
        read -r -s -p "Enter password for role '$APP_USER': " APP_PASS
        echo
    fi

    # 9-2) admin psql 커맨드 선택 (sudo 우선, 실패 시 로컬 postgres 계정)
    local ADMIN_PSQL=("sudo" "-u" "postgres" "psql" "-v" "ON_ERROR_STOP=1" "-X" "-q")
    if ! printf "\\q\\n" | "${ADMIN_PSQL[@]}" >/dev/null 2>&1; then
        ADMIN_PSQL=("psql" "-h" "${DEFAULT_HOST:-localhost}" "-p" "${DEFAULT_PORT:-5432}" "-U" "postgres" "-v" "ON_ERROR_STOP=1" "-X" "-q")
        if ! printf "\\q\\n" | "${ADMIN_PSQL[@]}" >/dev/null 2>&1; then
            echo "[Error] Can't connect as postgres (sudo or local). Set up access and retry."
            return 1
        fi
    fi

    # 9-3) 도우미: admin 쿼리 실행
    _admin_sql() { "${ADMIN_PSQL[@]}" -d "${2:-postgres}" -c "$1"; }

    # 9-4) 역할 생성/비번 설정 (존재하면 비번만 보정)
    local ROLE_EXISTS
    ROLE_EXISTS=$("${ADMIN_PSQL[@]}" -d postgres -tAc "SELECT 1 FROM pg_roles WHERE rolname='${APP_USER}'" || true)
    if [[ "$ROLE_EXISTS" != "1" ]]; then
        echo "==> Creating role '${APP_USER}'..."
        _admin_sql "CREATE ROLE ${APP_USER} LOGIN PASSWORD \$\$${APP_PASS}\$\$;"
    else
        echo "==> Role '${APP_USER}' exists. Ensuring password..."
        _admin_sql "ALTER ROLE ${APP_USER} WITH LOGIN PASSWORD \$\$${APP_PASS}\$\$;"
    fi

    # 9-5) DB 생성 (없으면)
    local DB_EXISTS
    DB_EXISTS=$("${ADMIN_PSQL[@]}" -d postgres -tAc "SELECT 1 FROM pg_database WHERE datname='${DB_NAME}'" || true)
    if [[ "$DB_EXISTS" != "1" ]]; then
        echo "==> Creating database '${DB_NAME}' owned by '${APP_USER}'..."
        _admin_sql "CREATE DATABASE ${DB_NAME} OWNER ${APP_USER} TEMPLATE template0 ENCODING 'UTF8';"
    else
        echo "==> Database '${DB_NAME}' already exists. Skipping create."
    fi

    # 9-6) 최소 하드닝 & 권한
    echo "==> Hardening connect privileges..."
    _admin_sql "REVOKE CONNECT ON DATABASE ${DB_NAME} FROM PUBLIC;"
    _admin_sql "GRANT  CONNECT ON DATABASE ${DB_NAME} TO ${APP_USER};"

    echo "==> Schema ownership & privileges..."
    _admin_sql "ALTER SCHEMA public OWNER TO ${APP_USER};" "${DB_NAME}"
    _admin_sql "REVOKE CREATE ON SCHEMA public FROM PUBLIC;" "${DB_NAME}"
    _admin_sql "GRANT USAGE, CREATE ON SCHEMA public TO ${APP_USER};" "${DB_NAME}"

    # 9-7) 요약/연동 안내
    echo "==> Done. Created/validated '${DB_NAME}' for user '${APP_USER}'."
    if [[ -n "$SERVICE_HIT" ]]; then
        echo "    Tip: psqlhelp ${SERVICE_HIT} \\l"
        echo "         psql_${SERVICE_HIT} -c '\\dt'"
    else
        echo "    (No service matched this DB in services[]. Consider adding one for convenience.)"
    fi
}

# -------------------------------
# 10) Delete DB & User safely (psql_delete <SERVICE_NAME>)
# -------------------------------
psql_delete() {
    local SERVICE_NAME="${1:-}"
    if [[ -z "$SERVICE_NAME" ]]; then
        echo "Usage: psql_delete <SERVICE_NAME>"
        echo "Example: psql_delete sleassem_dev"
        return 1
    fi

    # 10-1) 서비스 정보 조회
    local DB_NAME="" APP_USER="" FOUND=false
    for entry in "${services[@]}"; do
        read -r svc db user pass <<<"$entry"
        if [[ "$svc" == "$SERVICE_NAME" ]]; then
            DB_NAME="$db"
            APP_USER="$user"
            FOUND=true
            break
        fi
    done

    if [[ "$FOUND" == "false" ]]; then
        echo "[Error] Service '$SERVICE_NAME' not found in services list."
        echo "        Please check 'psqlhelp' for available services."
        return 1
    fi

    # 10-2) 삭제 확인 (Safety Check)
    echo "========================================================"
    echo " [WARNING] You are about to DELETE a database service."
    echo " Service : $SERVICE_NAME"
    echo " Database: $DB_NAME"
    echo " User    : $APP_USER"
    echo "========================================================"
    read -r -p "Are you sure you want to DESTROY this database? (Type 'delete'): " CONFIRM
    if [[ "$CONFIRM" != "delete" ]]; then
        echo "Aborted."
        return 0
    fi

    # 10-3) Admin 연결 설정 (psql_create와 동일 로직)
    local ADMIN_PSQL=("sudo" "-u" "postgres" "psql" "-v" "ON_ERROR_STOP=1" "-X" "-q")
    if ! printf "\\q\\n" | "${ADMIN_PSQL[@]}" >/dev/null 2>&1; then
        ADMIN_PSQL=("psql" "-h" "${DEFAULT_HOST:-localhost}" "-p" "${DEFAULT_PORT:-5432}" "-U" "postgres" "-v" "ON_ERROR_STOP=1" "-X" "-q")
        if ! printf "\\q\\n" | "${ADMIN_PSQL[@]}" >/dev/null 2>&1; then
            echo "[Error] Can't connect as postgres superuser."
            return 1
        fi
    fi
    _admin_sql() { "${ADMIN_PSQL[@]}" -d "${2:-postgres}" -c "$1"; }

    # 10-4) 활성 세션 강제 종료 (DB 삭제를 위해 필수)
    echo "==> Terminating active connections to '$DB_NAME'..."
    _admin_sql "SELECT pg_terminate_backend(pid) FROM pg_stat_activity WHERE datname = '${DB_NAME}' AND pid <> pg_backend_pid();"

    # 10-5) DB 삭제
    echo "==> Dropping database '$DB_NAME'..."
    if _admin_sql "DROP DATABASE IF EXISTS ${DB_NAME};"; then
        echo "    Database deleted."
    else
        echo "[Error] Failed to drop database."
        return 1
    fi

    # 10-6) 유저 삭제 시도 (다른 DB가 공유 중이면 실패함 - 정상)
    echo "==> Attempting to drop role '$APP_USER'..."
    # 2>&1로 에러 메시지를 캡처하여 사용자 친화적으로 보여줌
    local DROP_USER_MSG
    if DROP_USER_MSG=$("${ADMIN_PSQL[@]}" -d postgres -c "DROP ROLE ${APP_USER};" 2>&1); then
        echo "    Role '$APP_USER' deleted (No other dependencies)."
    else
        # 에러가 'dependent' 관련이면 경고만 하고 넘어감
        if [[ "$DROP_USER_MSG" == *"dependent"* ]]; then
             echo "    [Skip] Role '$APP_USER' was NOT deleted because other databases still use it."
        else
             echo "    [Info] $DROP_USER_MSG"
        fi
    fi

    # ---------------------------------------------------------
    # [NEW] 10-7) 현재 쉘 세션 메모리에서 서비스 제거 (UX 개선)
    # ---------------------------------------------------------
    local i
    for i in "${!services[@]}"; do
        # 해당 서비스 이름으로 시작하는 항목 찾기
        if [[ "${services[i]}" =~ ^${SERVICE_NAME}[[:space:]] ]]; then
            unset 'services[i]'
            break
        fi
    done
    # 배열 인덱스 재정렬 (구멍 메우기)
    services=("${services[@]}")

    # ---------------------------------------------------------
    # 10-8) 마무리 안내
    # ---------------------------------------------------------
    echo ""
    echo "Done."
    echo "IMPORTANT: The service is removed from current shell, but..."
    echo "           You MUST remove the line for '$SERVICE_NAME' in ~/dotfiles/bash/app/postgresql.bash manually"
    echo "           to prevent it from reappearing next time you open a terminal."
}