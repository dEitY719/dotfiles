        echo "port=$DEFAULT_PORT"
        echo "dbname=$db_name"
        echo "user=$db_user"
        echo "password=$db_pass"
        echo
    } >>"$PGSERVICE_FILE"

    # Create function instead of alias
    _create_psql_service_function "$service_name"
done

